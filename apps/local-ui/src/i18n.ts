const catalogs: Record<Locale, Record<string, string>> = { 'zh-CN': zh, 'en-US': en };

let currentLocale: Locale = 'zh-CN';

export function setLocale(locale: Locale): void { currentLocale = locale; }
export function getLocale(): Locale { return currentLocale; }

export function t(key: string): string {
  const val = catalogs[currentLocale][key] ?? catalogs['zh-CN'][key];
  if (val === undefined) console.warn(`[i18n] missing key: ${key}`);
  return val ?? key;
}
